
using SenderAPI.RabbitMQ;

namespace SenderAPI
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            string host = builder.Configuration["RabbitMQ:Host"] ?? "localhost";
            string queue = builder.Configuration["RabbitMQ:Queue"] ?? "test_queue";
            builder.Services.AddSingleton<IRabbitMqService>(new RabbitMqService(host, queue));

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
